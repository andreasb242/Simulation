package ch.zhaw.simulation.model.xy;

import java.awt.Color;

public class SubModel {
	private String name;
	private Color color;

	public SubModel() {
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Color getColor() {
		return color;
	}

	public void setColor(Color color) {
		this.color = color;
	}

}
